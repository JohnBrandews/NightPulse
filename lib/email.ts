import nodemailer from 'nodemailer';

// Create transporter â€” supports Gmail (service) or any raw SMTP host.
// For Gmail: set SMTP_USER=youraddress@gmail.com, SMTP_PASS=your-app-password
// For Brevo/other: set SMTP_HOST, SMTP_PORT, SMTP_USER, SMTP_PASS
const createTransporter = () => {
  const host = process.env.SMTP_HOST || '';
  const isGmail =
    host.includes('gmail') ||
    (process.env.SMTP_USER || '').includes('@gmail.com');

  if (isGmail) {
    // Gmail: use the built-in service shorthand (handles TLS/port automatically)
    return nodemailer.createTransport({
      service: 'gmail',
      auth: {
        user: process.env.SMTP_USER,
        pass: process.env.SMTP_PASS,
      },
    });
  }

  // Generic SMTP (Brevo, Mailgun, SendGrid, etc.)
  const port = parseInt(process.env.SMTP_PORT || '587');
  return nodemailer.createTransport({
    host,
    port,
    secure: port === 465, // auto-use SSL for port 465
    auth: {
      user: process.env.SMTP_USER,
      pass: process.env.SMTP_PASS,
    },
    tls: {
      rejectUnauthorized: false, // allow self-signed certs in dev
    },
  });
};

interface SendEmailOptions {
  to: string;
  subject: string;
  html: string;
  text?: string;
  attachments?: Array<{ filename: string; content: Buffer }>;
}

export async function sendEmail({ to, subject, html, text, attachments }: SendEmailOptions): Promise<boolean> {
  try {
    const transporter = createTransporter();

    const mailOptions = {
      from: `"NightPulse" <${process.env.SMTP_FROM || process.env.SMTP_USER}>`,
      to,
      subject,
      text: text || html.replace(/<[^>]*>/g, ''),
      html,
      attachments: attachments?.map(att => ({
        filename: att.filename,
        content: att.content,
        encoding: 'base64',
      })),
    };

    await transporter.sendMail(mailOptions);
    return true;
  } catch (error) {
    console.error('Email send error:', error);
    return false;
  }
}

interface InvoiceEmailOptions {
  to: string;
  clientName: string;
  invoiceNumber: string;
  totalAmount: number;
  dueDate: Date;
  clubName: string;
  isReceipt?: boolean;
  pdfAttachment?: {
    filename: string;
    content: Buffer;
  };
}

export async function sendPasswordResetEmail({
  to,
  clientName,
  resetLink,
}: {
  to: string;
  clientName: string;
  resetLink: string;
}) {
  const transporter = createTransporter();
  const mailOptions = {
    from: process.env.SMTP_FROM || `"NightPulse" <${process.env.SENDER_EMAIL}>`,
    to,
    subject: 'Password Reset Request - NightPulse',
    html: `
      <div style="font-family: Arial, sans-serif; max-width: 600px; margin: 0 auto; padding: 20px; color: #333;">
        <h2 style="color: #6366f1;">NightPulse Password Reset</h2>
        <p>Hello ${clientName},</p>
        <p>We received a request to reset your password. Click the button below to set a new password:</p>
        <div style="text-align: center; margin: 30px 0;">
          <a href="${resetLink}" style="background-color: #6366f1; color: white; padding: 12px 24px; text-decoration: none; border-radius: 6px; font-weight: bold;">Reset Password</a>
        </div>
        <p>This link will expire in 1 hour. If you didn't request a password reset, you can safely ignore this email.</p>
        <hr style="border: none; border-top: 1px solid #eee; margin: 20px 0;" />
        <p style="font-size: 12px; color: #666;">Generated by NightPulse Auth System</p>
      </div>
    `,
  };

  try {
    const info = await transporter.sendMail(mailOptions);
    console.log('Password reset email sent: %s', info.messageId);
    return true;
  } catch (error) {
    console.error('Send password reset email error:', error);
    return false;
  }
}

